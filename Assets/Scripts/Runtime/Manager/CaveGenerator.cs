public class CaveGenerator : NetworkBehaviour
{
    public int wallThickness = 5;

    public Tilemap groundTilemap;
    public Tilemap wallTilemap;
    public Tilemap stairTilemap;
    public TileBase groundTile;
    public TileBase wallTile;
    public TileBase stairTile;
    void Start()
    {
        CaveManager.Instance.RefreshCurrentStairTile();
        DrawSelectedCave();
    }

    void DrawSelectedCave()
    {
        int caveNumber = CaveManager.Instance.GetCaveLevelFromNetwork(CaveManager.Instance.CurrentLocalCaveLevel);
        if(caveNumber == -1)
        caveNumber = Random.Range(0, CaveShapes.caves.Length);

        CaveManager.Instance.AddCaveLevel(CaveManager.Instance.CurrentLocalCaveLevel,caveNumber);

        int[,] selected = CaveShapes.caves[caveNumber];
        int[,] padded = AddWallPadding(selected, wallThickness);

        int height = padded.GetLength(0); // rows = Y
        int width = padded.GetLength(1);  // columns = X


        groundTilemap.ClearAllTiles();
        wallTilemap.ClearAllTiles();

        for (int y = 0; y < height; y++)
        {
            for (int x = 0; x < width; x++)
            {
                Vector3Int pos = new Vector3Int(x, height - 1 - y, 0); // 👈 Flip Y here!

                int val = padded[y, x]; // Notice [y, x], not [x, y]

                if (val == 1 || val == 2)
                    groundTilemap.SetTile(pos, groundTile);

                if (val == 0)
                {
                    wallTilemap.SetTile(pos, wallTile);
                    if (HasAdjacentGround(padded, y, x)) // also update this
                        groundTilemap.SetTile(pos, groundTile);
                }

                if (val == 2)
                {
                    stairTilemap.SetTile(pos, stairTile);
                    wallTilemap.SetTile(pos, wallTile);
                    if(CaveManager.Instance.localStairTiles.Count == 0)
                    CaveManager.Instance.AddStairTile(pos, CaveManager.SpecialTileType.UpStair);
                }
            }
        }



    }

    bool HasAdjacentGround(int[,] map, int y, int x)
    {
        int height = map.GetLength(0); // rows
        int width = map.GetLength(1);  // columns

        for (int dy = -1; dy <= 1; dy++)
        {
            for (int dx = -1; dx <= 1; dx++)
            {
                if (dx == 0 && dy == 0) continue;
                int ny = y + dy;
                int nx = x + dx;
                if (ny >= 0 && ny < height && nx >= 0 && nx < width)
                {
                    if (map[ny, nx] == 1 || map[ny, nx] == 2)
                        return true;
                }
            }
        }

        return false;
    }


    int[,] AddWallPadding(int[,] originalMap, int padding)
    {
        int ow = originalMap.GetLength(0);
        int oh = originalMap.GetLength(1);
        int nw = ow + padding * 2;
        int nh = oh + padding * 2;

        int[,] padded = new int[nw, nh];

        for (int x = 0; x < nw; x++)
            for (int y = 0; y < nh; y++)
                padded[x, y] = 0; // default wall

        for (int x = 0; x < ow; x++)
            for (int y = 0; y < oh; y++)
                padded[x + padding, y + padding] = originalMap[x, y];

        return padded;
    }
